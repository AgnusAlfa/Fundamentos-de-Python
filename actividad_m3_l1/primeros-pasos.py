# ITEM 2: Declarar variables simples

# Declaración de variables (Guardamos la información)
nombre = "Joao"    # Las comillas indican que es un texto (String)
edad = 30                   # Sin comillas, porque es un número entero (Integer)
ciudad = "La Serena"         # Otro texto (String)

# Mostrar el contenido (Usamos la función print)
print(nombre)
print(edad)
print(ciudad)

# ITEM 3: Realizar operaciones básicas

# Declaramos el primer número entero
numero_a = 15 
# Declaramos el segundo número entero
numero_b = 10 
# Realizamos la operación de suma y asignamos el resultado a una variable
resultado = numero_a + numero_b 
# Mostramos el valor del resultado en la consola
print(resultado)